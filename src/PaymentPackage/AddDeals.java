    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String dealID = jTextField3.getText().toString();
        String dealCriteria = jTextField1.getText().toString();
        Double discount = Double.parseDouble(jTextField2.getText());
        
        Deals dl  = new Deals();
        dl.setDeals(dealID, dealCriteria, discount);
        
        DataSerial dm = new DataSerial();
        dm.DealSerialization(dl);
        
        ArrayList <Deals> dealsList= null;
        DataSerial  showDeals = new DataSerial();
        dealsList = showDeals.dealDeserialization();
        int i =0;
        for(Deals mk :dealsList){
            jTable1.setValueAt(mk.getDealID(), i, 0);
            jTable1.setValueAt(mk.getOfferCriteria(), i, 1);
            jTable1.setValueAt(mk.getDiscount(), i, 2);            
            i++;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String id = jTable1.getValueAt(jTable1.getSelectedRow(),0).toString();                        
        String criteria = jTable1.getValueAt(jTable1.getSelectedRow(),1).toString();                        
        String discount = jTable1.getValueAt(jTable1.getSelectedRow(),2).toString();
        
        jTextField3.setText(id);
        jTextField1.setText(criteria);
        jTextField2.setText(discount);
    }//GEN-LAST:event_jTable1MouseClicked

